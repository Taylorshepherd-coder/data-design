	<p>
		User Case:
	</p>
	<p>
		Marie May is on her laptop already logged into her YouTube account and is ready to watch a new video that was just released by her favorite YouTuber. Marie May mostly watches this one specific YouTuber mainly because the YouTuber always has a good wide range of variety of different makeup styles and techniques being used. Marie also enjoys this specific YouTuber because she is always on time with when she posts her videos weekly and also the YouTuber not only uses Marie's main types of makeup, but the Youtuber also generally responds to comments made on her post in a reasonably fast manner and these are all things Marie May looks for when following a YouTuber and for them to be considered one of her "favorites." Marie May also looks for precision and the ability to do makeup done by the YouTuber. Marie May hopes that by watching and following along with the YouTuber in the video that she will be able to recreate the same style being presented in the video. So, Marie May watches the video in anticipation of learning new techniques. She follows along throughout the video, occasionally pausing and rewinding when necessary. After the video, Marie May is pleased with her work and the new techniques she has just learned by watching the video and is excited for another one to be released by her favorite Youtuber, the following week, at the same time.
	</p>
	<p>
		Interaction Flow:
	</p>
	<p>
		<ul>
			<li>Marie May logs onto her computer and types in Youtube.com</li>
			<li>YouTube pulls up it's link to it's website and gives the user a list of recommended videos to watch based on previously watched videos</li>
			<li>Marie May wants to watch a new video that was just realised by her favorite YouTuber, so Marie types in the YouTubers name into the browser on the web page.</li>
			<li>YouTube brings up the YouTubers page which has many different links to all the videos released by the maker of the videos on YouTube.</li>
			<li>Marie May looks for the most recent video uploaded by her favorite Youtuber off their page and clicks on the video to watch it.</li>
			<li>YouTube plays an advertisement before playing the actual video and gives the user the ability to skip it if preferred after a certain amount of seconds into the ad.</li>
			<li>Marie May skips the advertisement after watching 6 seconds of it.</li>
			<li>Youtube takes the user directly to the video originally wanting to be viewed before the advertisement.</li>
			<li>Marie May is able to watch the new video her favorite YouTuber has just released. She is able to pause the video at any time to take notes or attempt the style that is being shown. She is also able to fast forward through the video or rewind it as well.</li>
			<li>Marie May watches the video.</li>
		</ul>
